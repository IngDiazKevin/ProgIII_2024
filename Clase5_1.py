#Colecciones y Paquetes

#Upper -> Convierte a MAYUSCULAS
print("Hola Mundo".upper())
#LOWER -> Convierte a minusculasa
print("HOLA Mundo".lower())
#Capitalize -> Convierte la primer letra a MAYUS
print("hola mundo".capitalize())
#Title -> Modo titulo
print("hola mundo, hola amigos".title())

#Count = Contador
print("Hola mundo mundo mundo".count("mundo"))

#Find = Encontrar la ubicacion del caract/subcaneda
print("HOla mundos".find("amigo"))

#IsDigit ->  TODA la cadena deben ser numeros para TRUE
print("6789".isdigit())

#IsAlnum -> TRUE cuando son letras o numeros
print("abc#".isalnum())

#IsAlpha -> True solo cuando toda la cadena son LETRAS
print("abc".isalpha())

#IsUpper -> TRUE cuando TODA la cadena son MAYUSC
print("HOLA".isupper())

#IsSpace -> TRUE cuando TODA la cadena son ESPACIOS
print("hola mundo".isspace())

#StarSwith -> TRUE si empieza con el o los caracteres 
print("Hola Mundo".startswith(" "))

#Join -> Une todos los caracteres con OTRO caracter dado
print("5".join("Hola Mundo Cruel"))














